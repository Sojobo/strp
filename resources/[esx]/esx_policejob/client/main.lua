		title    = _U('garage_title'),
		align    = 'left',
		elements = elements
	}, function(data, menu)

		if data.current.action == 'buy_vehicle' then
			local shopElements, shopCoords = {}

			if type == 'car' then
				shopCoords = Config.PoliceStations[station].Vehicles[partNum].InsideShop
				local authorizedVehicles = Config.AuthorizedVehicles
                for k,vehicle in ipairs(authorizedVehicles) do
                    if vehicle.minrank <= PlayerData.job.grade then
                        table.insert(shopElements, {
                            label = ('%s - <span style="color:green;">%s</span>'):format(vehicle.label, _U('shop_item', ESX.Math.GroupDigits(vehicle.price))),
                            name  = vehicle.label,
                            model = vehicle.model,
                            price = vehicle.price,
                            type  = 'car'
                        })
                    end
                end
			elseif type == 'helicopter' then
				shopCoords = Config.PoliceStations[station].Helicopters[partNum].InsideShop
				local authorizedHelicopters = Config.AuthorizedHelicopters
                for k,vehicle in ipairs(authorizedHelicopters) do
                    if vehicle.minrank <= PlayerData.job.grade then
                        table.insert(shopElements, {
                            label = ('%s - <span style="color:green;">%s</span>'):format(vehicle.label, _U('shop_item', ESX.Math.GroupDigits(vehicle.price))),
                            name  = vehicle.label,
                            model = vehicle.model,
                            price = vehicle.price,
                            livery = vehicle.livery or nil,
