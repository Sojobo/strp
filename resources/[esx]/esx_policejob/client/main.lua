function SetDutyStatus(setStatus)
  if setStatus == false then
    ESX.TriggerServerCallback('esx_service:isInService', function(isInService)
      if isInService then

        playerInService = false

        local notification = {
            subject = _U('service_anonunce'),
            msg = _U('service_out_announce', PlayerData.job.grade_label .. " " .. GetPlayerName(PlayerId())),
            icon = 'fas fa-info',
            iconStyle = 'blue'
        }

        TriggerServerEvent('esx_service:notifyAllInService', notification, 'police')

        TriggerServerEvent('esx_service:disableService', 'police')
        TriggerServerEvent('esx_policejob:forceBlip')
        TriggerEvent('esx_service:removeServiceBlips')
       ESX.ShowNotification (_U('service_out'))
      end
    end, 'police')
  elseif setStatus == true then
    local serviceOk = 'waiting'

    ESX.TriggerServerCallback('esx_service:isInService', function(isInService)
      if not isInService then

        ESX.TriggerServerCallback('esx_service:enableService', function(canTakeService, maxInService, inServiceCount)
          if not canTakeService then
            ESX.ShowNotification(_U('service_max', inServiceCount, maxInService))
          else

            serviceOk = true
            playerInService = true

            local notification = {
                subject = _U('service_anonunce'),
                msg = _U('service_in_announce', PlayerData.job.grade_label .. " " .. GetPlayerName(PlayerId())),
                icon = 'fas fa-info',
                iconStyle = 'blue'
            }

            TriggerServerEvent('esx_service:notifyAllInService', notification, 'police')
			TriggerServerEvent('esx_policejob:forceBlip')
            ESX.ShowNotification(_U('service_in'))
          end
        end, 'police')

      else
        serviceOk = true
      end
    end, 'police')

    while type(serviceOk) == 'string' do
      Citizen.Wait(5)
    end

    -- if we couldn't enter service don't let the player get changed
    if not serviceOk then
      return
    end
  end
end

